def generate_timetable(classes_data, teachers_data, days_list, periods_per_day):
    """
    Generate a clash-free timetable.
    
    classes_data: dict of {class_name: {subject: periods_per_week}}
    teachers_data: dict of {teacher_name: [list of subjects they teach]}
    days_list: list of day names
    periods_per_day: int
    
    Returns: dict of {class_name: {day: {period: subject}}}
             and dict of {teacher_name: {day: {period: class_name}}}
    """
    
    # Initialise empty grids
    class_grid = {}
    teacher_grid = {}
    
    for cls in classes_data:
        class_grid[cls] = {day: {p: None for p in range(1, periods_per_day + 1)} for day in days_list}
    
    for teacher in teachers_data:
        teacher_grid[teacher] = {day: {p: None for p in range(1, periods_per_day + 1)} for day in days_list}
    
    # Build reverse mapping: subject -> list of teachers
    subject_to_teachers = {}
    for teacher, subjects in teachers_data.items():
        for subj in subjects:
            if subj not in subject_to_teachers:
                subject_to_teachers[subj] = []
            subject_to_teachers[subj].append(teacher)
    
    # For each class, schedule subjects
    for cls, subjects_dict in classes_data.items():
        # Create a list of all subject slots to fill
        all_slots = []
        for subject, count in subjects_dict.items():
            for _ in range(count):
                all_slots.append(subject)
        
        # Shuffle for randomness
        random.shuffle(all_slots)
        
        # Get available days and periods
        all_day_periods = []
        for day in days_list:
            for p in range(1, periods_per_day + 1):
                all_day_periods.append((day, p))
        random.shuffle(all_day_periods)
        
        slot_idx = 0
        for subject in all_slots:
            placed = False
            
            # Try to find a slot for this subject
            for day, p in all_day_periods:
                if class_grid[cls][day][p] is not None:
                    continue
                
                # Find a teacher for this subject
                teachers_for_subj = subject_to_teachers.get(subject, [])
                random.shuffle(teachers_for_subj)
                
                for teacher in teachers_for_subj:
                    if teacher_grid[teacher][day][p] is None:
                        # Place it!
                        class_grid[cls][day][p] = (subject, teacher)
                        teacher_grid[teacher][day][p] = cls
                        placed = True
                        break
                
                if placed:
                    break
            
            if not placed:
                # Fallback: force place in first empty slot
                for day, p in all_day_periods:
                    if class_grid[cls][day][p] is None:
                        teachers_for_subj = subject_to_teachers.get(subject, ["Unassigned"])
                        teacher = teachers_for_subj[0] if teachers_for_subj else "Unassigned"
                        class_grid[cls][day][p] = (subject, teacher)
                        if teacher != "Unassigned":
                            teacher_grid[teacher][day][p] = cls
                        placed = True
                        break
    
    return class_grid, teacher_grid


def grid_to_dataframe(class_grid, days_list, periods_per_day):
    """Convert the class grid to a flat dataframe for display."""
    rows = []
    for cls, grid in class_grid.items():
        for day in days_list:
            for p in range(1, periods_per_day + 1):
                val = grid[day][p]
                if val is not None:
                    subject, teacher = val
                    rows.append({
                        'Class': cls,
                        'Day': day,
                        'Period': p,
                        'Subject': subject,
                        'Teacher': teacher
                    })
    return pd.DataFrame(rows)


