def generate_pdf(df, title):
    html_rows = ""
    for _, row in df.iterrows():
        html_rows += "<tr>" + "".join(f"<td>{str(val)}</td>" for val in row) + "</tr>"
    columns = "".join(f"<th>{col}</th>" for col in df.columns)
    html_content = f"""
    <!DOCTYPE html><html><head><meta charset="UTF-8"><title>{title}</title>
    <style>
        body {{ font-family: Arial, sans-serif; padding: 20px; }}
        h2 {{ color: #004d4d; text-align: center; }}
        table {{ width: 100%; border-collapse: collapse; margin-top: 20px; }}
        th {{ background-color: #006666; color: white; padding: 10px; text-align: left; }}
        td {{ padding: 8px; border-bottom: 1px solid #ddd; }}
        tr:nth-child(even) {{ background-color: #f2f2f2; }}
        .footer {{ text-align: center; margin-top: 30px; font-size: 12px; color: #888; }}
    </style></head><body>
        <h2>{title}</h2>
        <table><thead><tr>{columns}</tr></thead><tbody>{html_rows}</tbody></table>
        <div class="footer">Generated by VPS Timetable Manager on {datetime.date.today()}</div>
    </body></html>
    """
    b64 = base64.b64encode(html_content.encode()).decode()
    href = f'<a href="data:text/html;base64,{b64}" download="{title.replace(" ", "_")}.html" style="display: inline-block; padding: 10px 20px; background-color: #006666; color: white; text-decoration: none; border-radius: 8px; font-weight: bold;">📥 Download as HTML (Print to PDF)</a>'
    return href

